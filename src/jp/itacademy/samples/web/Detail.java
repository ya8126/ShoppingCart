package jp.itacademy.samples.web;

public class Detail {

	private int no;
	private int quantity;
	
	public Detail(int no, int quantity) {
		this.no = no;
		this.quantity = quantity;
	}
	
	public int getNo() {
		return no;
	}
	
	public int getQuantity() {
		return quantity;
	}

}
